# division al piso
x = 6
y = 2
z = 7

print(f"{z} dividido al piso entre {y} es igual a {z/y}")
# para descriminar los decimales se pone //
print(f"{z} dividido al piso entre {y} es igual a {z//y}")

# modulo o resto de una division
print(f"{z} modulo de {y} es igual a {z%y}")
#potencias
print(f"{x} elevado a {y} es igual a {x**y}")
#al cubo
print(f"{x} elevado a {y} es igual a {x**3}")
# raiz cuadrada
print(f"La raiz cuadrada {x} es igual a {x**0.5}")